export const facebook = new RegExp(
  /^(http(s)?:\/\/)?((w{3}\.)?)facebook.com\/.+?$/
);
export const twitter = new RegExp(
  /^(http(s)?:\/\/)?((w{3}\.)?)twitter\.com\/(#!\/)?[a-zA-Z0-9_]+$/
);
export const linkedin = new RegExp(
  /^(http(s)?:\/\/)?([\w]+\.)?linkedin\.com\/(pub|in|profile)(\/\w+)?$/
);
